function greet(occasion ){
    switch(occasion){
        case "New year":
            console.log("happy "+occasion);
            break;

        case "Chrismas":
            console.log("Merry "+occasion);
            break;

        case "Birthday":
            console.log("Happy "+occasion);
            break;
        
        case "Anniversary":
            console.log("Happy "+occasion);
            break;
    }  
}
greet("New year")
greet("Chrismas")
greet("Birthday")
greet("Anniversary")